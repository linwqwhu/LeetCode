    public boolean isValidSudoku(char[][] board) {
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                if (board[i][j] != '.') {
                    //检查行是否符合
                    for (int k = j + 1; k < 9; k++) {
                        if (board[i][j] == board[i][k]) {
                            return false;
                        }
                    }
                    //检查列是否符合
                    for (int k = i + 1; k < 9; k++) {
                        if (board[i][j] == board[k][j]) {
                            return false;
                        }
                    }

                    //检查3*3宫格内是否符合
                    int start_x = i / 3 * 3;
                    int start_y = j / 3 * 3;
                    for (int x = start_x; x < start_x + 3; x++) {
                        for (int y = start_y; y < start_y + 3; y++) {
                            if (x != i && y != j && board[i][j] == board[x][y]) {
                                return false;
                            }
                        }
                    }
                }
            }
        }
        return true;
    }

